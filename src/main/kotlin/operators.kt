fun main(){
    //Arithmetic Operators
    var num1=60
    var num2=20
    println(num1+num2)
    println(num1-num2)
    println(num1*num2)
    println(num1/num2)
    println(num1%num2) //Modulus

    //Assignment Operators
    var x=6
    x+=5
    println(x)
    x-=1
    println(x)

    var y=10
    y-=3
    println(y)
    y*=2
    println(y)

    var m=9
    m/=3
    println(m)

    var n=64
    n%=10
    println(n)

    //Comparison Operators (returns a boolean)
    var b=20
    println(b==2)
    println(b<2)
    println(b>2)
    println(b<=2)
    println(b>=2)
    println(" ")
    println("Logical Operators")

    //Logical Operators
    var mylogic=80
    println(mylogic<50 && mylogic==80) //AND
    println(mylogic<50 || mylogic==80) //OR
    println(!(mylogic<50 || mylogic==80)) //NOT



}


